7. รอบจ่ายเงิน เงื่อนไขการจ่าย และภาษีหัก ณ ที่จ่าย
• บริษัทจะจ่ายค่าตอบแทนตามรอบจ่ายที่บริษัทกำหนด เช่น วันที่ 10 และ 25 ของเดือน หรือรอบอื่นที่ตกลงกันเป็นลายลักษณ์อักษร
• ค่าตอบแทนจะจ่ายเฉพาะงานที่ปิดสมบูรณ์ในระบบ มีรูปครบ สถานะครบ ข้อมูลครบ และผ่านการตรวจสอบจากบริษัทแล้ว
• กรณีมีข้อร้องเรียน งานเสียหาย ข้อมูลไม่ครบ งานรับประกัน หรือมีรายการต้องตรวจสอบ
บริษัทมีสิทธิชะลอการจ่ายเฉพาะงานนั้นจนกว่าจะตรวจสอบแล้วเสร็จ
• ค่าตอบแทนในตารางเรทถือเป็นยอด ก่อนหักภาษี ณ ที่จ่าย เว้นแต่บริษัทแจ้งเป็นอย่างอื่นเป็นลายลักษณ์อักษร
• กรณีที่กฎหมายกำหนดให้บริษัทมีหน้าที่หักภาษี ณ ที่จ่าย บริษัทมีสิทธิหักภาษี ณ ที่จ่ายจากค่าตอบแทนของพาร์ทเนอร์ในอัตราที่กฎหมายกำหนด
เช่น 3% สำหรับค่าบริการหรือค่าจ้างทำของ และนำส่งกรมสรรพากรในนามผู้รับเงิน
• บริษัทจะออกหนังสือรับรองการหักภาษี ณ ที่จ่ายให้พาร์ทเนอร์ เพื่อใช้เป็นหลักฐานประกอบการยื่นภาษีประจำปี
• หากพาร์ทเนอร์เป็นบุคคลธรรมดา โดยทั่วไปบริษัทจะใช้แบบ ภ.ง.ด.3 ตามเงื่อนไขที่กฎหมายกำหนด และหากพาร์ทเนอร์เป็นนิติบุคคล
โดยทั่วไปบริษัทจะใช้แบบ ภ.ง.ด.53
• หากพาร์ทเนอร์จดทะเบียนภาษีมูลค่าเพิ่ม ต้องแจ้งบริษัทล่วงหน้าและออกใบกำกับภาษี/เอกสารตามกฎหมายให้ถูกต้องก่อนรับเงิน
• ตัวอย่าง: ค่าช่างพาร์ทเนอร์ 10,000 บาท หัก ณ ที่จ่าย 3% = 300 บาท บริษัทโอนให้ช่าง 9,700 บาท และนำส่งกรมสรรพากร 300 บาท
ต้นทุนรวมของบริษัทตามงานนี้ยังเท่ากับ 10,000 บาท ไม่ใช่ 10,300 บาท
• กรณีมีเงินประกันรายเดือน: ให้คำนวณภาษีหัก ณ ที่จ่ายจากค่าตอบแทนก่อน แล้วจึงหักเงินประกันตามยอดที่ตกลง เช่น ค่าตอบแทน 10,000 บาท
หักภาษี 300 บาท หักเงินประกัน 1,000 บาท บริษัทโอนสุทธิ 8,700 บาท และเงินประกัน 1,000 บาทยังเป็นเงินของพาร์ทเนอร์ตามเงื่อนไขสัญญา

