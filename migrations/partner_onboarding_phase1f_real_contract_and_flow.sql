• หากพาร์ทเนอร์ต้องการยุติการร่วมงาน ควรแจ้งล่วงหน้าอย่างน้อย 15 วัน และต้องเคลียร์งานค้าง งานรับประกัน อุปกรณ์
หรือยอดเงินค้างทั้งหมดก่อน

• บริษัทมีสิทธิยุติสัญญาทันที หากพาร์ทเนอร์ทิ้งงาน รับเงินนอกระบบ ทำให้ลูกค้าหรือบริษัทเสียหายร้ายแรง เปิดเผยข้อมูลภายใน แอบอ้างชื่อบริษัท
หรือผิดเงื่อนไขสำคัญของสัญญา
• หลังยุติสัญญา พาร์ทเนอร์ยังต้องรับผิดชอบงานที่ทำไว้ก่อนหน้า ข้อร้องเรียน งานรับประกัน และความเสียหายที่เกิดจากการกระทำของตน

17. การแก้ไขสัญญา เรทค่าตอบแทน และข้อกฎหมาย
• บริษัทสามารถปรับปรุงเรทค่าตอบแทน เงื่อนไขการจ่าย รอบจ่าย หรือมาตรฐานงานได้ตามต้นทุน โปรโมชัน สภาพตลาด
และความเหมาะสมในการบริหารงาน

• หากมีการเปลี่ยนแปลงสำคัญ บริษัทจะแจ้งให้พาร์ทเนอร์ทราบก่อนนำไปใช้กับงานใหม่
• งานที่รับไว้ก่อนมีการเปลี่ยนแปลง ให้ยึดตามเงื่อนไขที่ตกลงไว้ในงานนั้น เว้นแต่ทั้งสองฝ่ายตกลงใหม่
• หากมีการเปลี่ยนแปลงกฎหมายภาษีหรือข้อกำหนดราชการ บริษัทสามารถปรับวิธีจ่ายเงิน เอกสาร หรือการหักภาษีให้สอดคล้องกับกฎหมายได้ทันที
